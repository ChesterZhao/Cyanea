public class Tentacle {
    int longValue;
    int TentacleNum;

    public Tentacle(int longValue, int TentacleNum) {
        this.longValue = longValue;
        this.TentacleNum = TentacleNum;
    }

    public int getLongValue() {
        return longValue;
    }

    public void setLongValue(int longValue) {
        this.longValue = longValue;
    }

    public int getTentacleNum() {
        return TentacleNum;
    }

    public void setTentacleNum(int tentacleNum) {
        TentacleNum = tentacleNum;
    }
}
