public class Head {
    String texture;
    String size;

    public Head(String texture, String size) {
        this.texture = texture;
        this.size = size;
    }

    public String getTexture() {
        return texture;
    }

    public String getSize() {
        return size;
    }

    public void setTexture(String texture) {
        this.texture = texture;
    }

    public void setSize(String size) {
        this.size = size;
    }
}
